# "for" Schleife. Ähnlich der "while" Schleife, aber andere Schreibweise und Einsatzzweck
# Verwendeung der "for" Schleife wenn Zahlen dargestellt werden sollen und wenn Einträge
# in einer Liste verwendet werden sollen
for i in range(0, 10):      # Ausgabe inklusive de "0", exklusive der "10"
    print(i)

for j in range(0, 10):
    print(j * 2)            # Weiterverarbeitung der Variable "j" * 2

liste = [5, 8, 10]
for k in liste:
    print(k)                # Ausgabe untereinander der Varaible liste


